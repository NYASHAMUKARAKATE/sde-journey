// Arithmetic
console.log(10 + 5); 
console.log(10 - 5); 
console.log(10 * 5); 
console.log(10 / 5); 
console.log(10 % 3); 
console.log(2 ** 3); 

// Comparison
console.log(5 == "5");  
console.log(5 === "5"); 
console.log(5 != "5");  
console.log(5 !== "5"); 

// Logical
console.log(true && false); 
console.log(true || false); 
console.log(!true);        

// Ternary
const age = 20;
const status = age >= 18 ? "Adult" : "Minor";
console.log(status);